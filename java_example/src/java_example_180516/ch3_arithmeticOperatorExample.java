package java_example_180516;

public class ch3_arithmeticOperatorExample {

	public static void main(String[] args) {
		int v1 = 5;
		int v2 = 2;
		
		int result1 = v1 + v2;
		System.out.println("result = " + result1);	//7
		
		int result2 = v1 - v2;
		System.out.println("result = " + result2);	//3
		
		int result3 = v1 * v2;
		System.out.println("result = " + result3);	//10
		
		int result4 = v1 / v2;
		System.out.println("result = " + result4);	//2
		
		int result5 = v1 % v2;
		System.out.println("result = " + result5);	//1
		
		double result6 = (double) v1 / v2;
		System.out.println("result = " + result6);	//2.5

	}

}
